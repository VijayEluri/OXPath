package uk.ac.ox.comlab.oxpath;

import java.io.IOException;

import org.xml.sax.EntityResolver;
import org.xml.sax.InputSource;
import org.xml.sax.SAXException;

/**
 * Implements EntityResolver so that local XML Entity Resolution occurs, as W3C now blocks this traffic
 * @author AndrewJSel
 *
 */
public class OXPathEntityResolver implements EntityResolver {

	/* (non-Javadoc)
	 * @see org.xml.sax.EntityResolver#resolveEntity(java.lang.String, java.lang.String)
	 */
	@Override
	public InputSource resolveEntity(String arg0, String arg1)
			throws SAXException, IOException {
		if (arg1.equals("http://www.w3.org/TR/xhtml1/DTD/xhtml1-transitional.dtd"))
			return new InputSource("xhtml1-transitional.dtd");
//			return new InputSource("xhtml1-catalog.xml");
		else if (arg1.equals("http://www.w3.org/TR/html4/loose.dtd")) return new InputSource("loose.dtd");
		else return null;
	}

}
